public class Main
{
    public static void main(String[] args)
    {
        //below these are the primitive data types except string data types
        byte bt=70; //byte data type size 1 byte
        int x=20; //integer data type size 4 byte
        float y=30.22f; //floating data type size 4 byte
        short z=40;    //short data type 2 byte
        long a=2000L;  //long data type 8 byte
        double b=40.45d; //double data type 8 byte
        boolean myBool=true; //boolean data type 1 bit
        char letter='S'; //char data type 2 byte
        String name="Vinay Shukla"; //string data type
        System.out.println("below these are the primitive data types except string data types");
        System.out.println(bt);
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        System.out.println(a);
        System.out.println(b);
        System.out.println(myBool);
        System.out.println(letter);
        System.out.println(name);

    }
}