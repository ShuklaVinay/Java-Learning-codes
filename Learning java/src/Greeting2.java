public class Greeting2
{
    public static void main(String[] args)
    {
        //in java + operate use for both addition for numbers & concat for string
        //+ operator use addition for numbers
        int x=20;
        int y=30;
        int z=x+y;

        System.out.println(z);
        //+ operator use for concat string
        String a="20";
        String b="30";
        String c=a+b;
        System.out.println(c);
    }
}
